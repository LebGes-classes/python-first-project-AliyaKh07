from utils import clear_screen


class Menu:
    """Управляет отображением и обработкой пользовательского ввода в меню."""

    @staticmethod
    def show_main_menu() -> str:
        """Отображает главное меню.

        Returns:
            Выбор пользователя как строка.
        """

        clear_screen()

        print("┌──────────────────────────────┐")
        print("│        ЛАБИРИНТ v1.0        │")
        print("├──────────────────────────────┤")
        print("│ 1. Легкий уровень (9×7)     │")
        print("│ 2. Средний уровень (13×11)  │")
        print("│ 3. Сложный уровень (17×15)  │")
        print("│ 4. Выход                    │")
        print("└──────────────────────────────┘")

        return input("Выберите уровень: ").strip()

    @staticmethod
    def show_pause_menu() -> str:
        """Отображает меню паузы.

        Returns:
            Выбор пользователя как строка.
        """

        print("\n[Пауза]")
        print("1. Продолжить")
        print("2. В главное меню")

        return input("Ваш выбор: ").strip()

